/* ---- Auto-advance VredeVeranda slider ---- */
(function () {
  if (reduceMotion) return;
  const trackId = 'vv-track';
  const track = document.getElementById(trackId);
  if (!track) return;

  let current = 0;
  const slides = track.querySelectorAll('.screenshot-slide');
  const dots = document.querySelectorAll('[data-track="' + trackId + '"]');

  setInterval(function () {
    current = (current + 1) % slides.length;
    slides.forEach(function (slide, i) { slide.classList.toggle('active', i === current); });
    dots.forEach(function (dot, i) { dot.classList.toggle('active', i === current); });
  }, 3500);
}());

/* ---- Contact form → opens email client pre-filled (no backend needed) ---- */
(function () {
  const form = document.getElementById('contact-form');
  if (!form) return;
  const note = document.getElementById('form-note');
  const EMAIL = 'hello@vindex.software';

  function fieldEl(input) { return input.closest('.field'); }

  function ensureError(field) {
    let err = field.querySelector('.err');
    if (!err) {
      err = document.createElement('span');
      err.className = 'err';
      field.appendChild(err);
    }
    return err;
  }

  function validate(input, message) {
    const field = fieldEl(input);
    const valid = input.checkValidity() && input.value.trim() !== '';
    field.classList.toggle('invalid', !valid);
    ensureError(field).textContent = valid ? '' : message;
    return valid;
  }

  form.addEventListener('submit', function (e) {
    e.preventDefault();
    const name = form.elements.name;
    const email = form.elements.email;
    const type = form.elements.type;
    const message = form.elements.message;

    const okName = validate(name, vxT('js.err_name', 'Please tell us your name.'));
    const okEmail = validate(email, vxT('js.err_email', 'Please enter a valid email address.'));
    const okMsg = validate(message, vxT('js.err_message', 'Please add a few details about your project.'));

    if (!(okName && okEmail && okMsg)) {
      const firstInvalid = form.querySelector('.field.invalid input, .field.invalid textarea');
      if (firstInvalid) firstInvalid.focus();
      note.classList.remove('ok');
      note.textContent = vxT('js.fix', 'Please fill in the highlighted fields.');
      return;
    }

    const subject = vxT('js.subject', 'New project enquiry — ') + type.value;
    const body =
      vxT('js.label_name', 'Name') + ': ' + name.value + '\n' +
      vxT('js.label_email', 'Email') + ': ' + email.value + '\n' +
      vxT('js.label_looking', 'Looking for') + ': ' + type.value + '\n\n' +
      message.value + '\n';

    window.location.href =
      'mailto:' + EMAIL +
      '?subject=' + encodeURIComponent(subject) +
      '&body=' + encodeURIComponent(body);

    note.classList.add('ok');
    note.textContent = vxT('js.opening', 'Opening your email app… if nothing happens, email us at ') + EMAIL;
  });

  // Clear error state as the user fixes a field
  ['name', 'email', 'message'].forEach(function (n) {
    const input = form.elements[n];
    input.addEventListener('input', function () {
      const field = fieldEl(input);
      if (field.classList.contains('invalid') && input.value.trim() !== '') {
        field.classList.remove('invalid');
        const err = field.querySelector('.err');
        if (err) err.textContent = '';
      }
    });
  });
}());

/* ---- Hero: count-up KPI numbers ---- */
(function () {
  const visual = document.querySelector('.hero-visual');
  if (!visual) return;
  const nums = visual.querySelectorAll('.kpi-num');

  function countUp(el) {
    const to = parseFloat(el.dataset.to);
    const dec = parseInt(el.dataset.dec || '0', 10);
    const pre = el.dataset.prefix || '';
    const suf = el.dataset.suffix || '';
    const duration = 1600;
    const start = performance.now();
    function tick(now) {
      const p = Math.min(1, (now - start) / duration);
      const eased = 1 - Math.pow(1 - p, 3); // ease-out cubic
      el.textContent = pre + (to * eased).toFixed(dec) + suf;
      if (p < 1) requestAnimationFrame(tick);
      else el.textContent = pre + to.toFixed(dec) + suf;
    }
    requestAnimationFrame(tick);
  }

  function play() {
    if (reduceMotion) return; // values already shown in HTML
    nums.forEach(function (el) { el.textContent = (el.dataset.prefix || '') + '0' + (el.dataset.suffix || ''); });
    requestAnimationFrame(function () { nums.forEach(countUp); });
  }

  if (!('IntersectionObserver' in window)) { play(); return; }
  const io = new IntersectionObserver(function (entries) {
    entries.forEach(function (entry) {
      if (entry.isIntersecting) { play(); io.disconnect(); }
    });
  }, { threshold: 0.4 });
  io.observe(visual);
}());

/* ---- Dark / light theme toggle ---- */
(function () {
  const btn = document.getElementById('theme-toggle');
  if (!btn) return;
  function current() {
    return document.documentElement.getAttribute('data-theme') === 'dark' ? 'dark' : 'light';
  }
  function apply(theme) {
    document.documentElement.setAttribute('data-theme', theme);
    try { localStorage.setItem('theme', theme); } catch (e) {}
    btn.setAttribute('aria-label', theme === 'dark' ? 'Switch to light mode' : 'Switch to dark mode');
  }
  apply(current());
  btn.addEventListener('click', function () {
    apply(current() === 'dark' ? 'light' : 'dark');
  });
}());

/* ---- Scroll-spy: highlight the nav link for the section in view ---- */
(function () {
  if (!('IntersectionObserver' in window)) return;
  const links = Array.prototype.slice.call(document.querySelectorAll('.nav-links a[href^="#"]'));
  const map = {};
  const sections = [];
  links.forEach(function (a) {
    const id = a.getAttribute('href').slice(1);
    const sec = document.getElementById(id);
    if (sec) { map[id] = a; sections.push(sec); }
  });
  if (!sections.length) return;
  const spy = new IntersectionObserver(function (entries) {
    entries.forEach(function (e) {
      if (e.isIntersecting) {
        links.forEach(function (l) { l.classList.remove('active'); });
        if (map[e.target.id]) map[e.target.id].classList.add('active');
      }
    });
  }, { rootMargin: '-45% 0px -50% 0px', threshold: 0 });
  sections.forEach(function (s) { spy.observe(s); });
}());

