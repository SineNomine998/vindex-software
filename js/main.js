function vxLang() {
  return document.documentElement.getAttribute('lang') === 'nl' ? 'nl' : 'en';
}
function vxT(key, fallback) {
  return (vxLang() === 'nl' && VX_NL[key]) ? VX_NL[key] : fallback;
}

(function () {
  var nodes = document.querySelectorAll('[data-i18n]');
  nodes.forEach(function (el) { el._vxEn = el.textContent; });
  var phNodes = document.querySelectorAll('[data-i18n-ph]');
  phNodes.forEach(function (el) { el._vxEnPh = el.getAttribute('placeholder'); });

  var metaDesc = document.querySelector('meta[name="description"]');
  var enTitle = document.title;
  var enDesc = metaDesc ? metaDesc.getAttribute('content') : '';
  var toggle = document.getElementById('lang-toggle');

  function apply(lang) {
    document.documentElement.setAttribute('lang', lang);
    nodes.forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      el.textContent = (lang === 'nl' && VX_NL[key]) ? VX_NL[key] : el._vxEn;
    });
    phNodes.forEach(function (el) {
      var key = el.getAttribute('data-i18n-ph');
      el.setAttribute('placeholder', (lang === 'nl' && VX_NL[key]) ? VX_NL[key] : el._vxEnPh);
    });
    document.title = (lang === 'nl' && VX_NL['meta.title']) ? VX_NL['meta.title'] : enTitle;
    if (metaDesc) metaDesc.setAttribute('content', (lang === 'nl' && VX_NL['meta.desc']) ? VX_NL['meta.desc'] : enDesc);
    try { localStorage.setItem('lang', lang); } catch (e) {}
    if (toggle) {
      toggle.textContent = lang === 'nl' ? 'EN' : 'NL';
      toggle.setAttribute('aria-label', lang === 'nl' ? 'Switch to English' : 'Schakel naar het Nederlands');
    }
  }

  var saved;
  try { saved = localStorage.getItem('lang'); } catch (e) {}
  if (saved !== 'nl' && saved !== 'en') {
    saved = (navigator.language || '').toLowerCase().indexOf('nl') === 0 ? 'nl' : 'en';
  }
  apply(saved);
  if (toggle) {
    toggle.addEventListener('click', function () { apply(vxLang() === 'nl' ? 'en' : 'nl'); });
  }
}());

